#  user will input (3 ages). find the oldest one?

age_1 = int(input("enter 1st age: "))
age_2 = int(input("enter 2st age: "))
age_3 = int(input("enter 3st age: "))
if age_1>=age_2 and age_1>=age_3:
    print("greatest age 1", age_1)
elif age_2>=age_1 and age_2>=age_3:
    print("greatest age 2", age_2)
else:
    print("greatest age 3", age_3)


                          
    
   
     
    